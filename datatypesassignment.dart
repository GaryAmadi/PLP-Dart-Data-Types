//Data Types in Dart
void main() {
  //Int to store whole numbers
  int age = 21;
  print("I am $age years old");

  //double for storing decimals
  double rate = 22.58;
  print("The interest rate is $rate");

  //String for storing text
  String name = "Gary Amadi";
  print("My name is $name");

  //List for storing a collection
  List<String> players = ["Saliba", "Rice", "Saka"];
  print("Arsenal players: $players");

  //Map for storing key pair values
  Map<String, int> playerages = {"Saliba": 22, "Rice": 25, "Saka": 22};
  print("Arsenal player ages: $playerages");
}
